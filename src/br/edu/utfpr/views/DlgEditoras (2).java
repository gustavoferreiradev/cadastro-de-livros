    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Editoras editoras = new Editoras();
        EditorasDAO dao = new EditorasDAO();
        editoras.setEditora(tfEditora.getText());
        
        if (tfCodEditora.getText().isEmpty()) {
            dao.inserir(editoras);
            editorasTableModel.addEditora(pesquisar(editoras));
            
        } else {
            editoras.setCodEditora(Integer.parseInt(tfCodEditora.getText()));
            dao.atualizar(editoras);
            editorasTableModel.updateEditora(linhaSelecionada, editoras);
            
        }
        this.setVisible(false);
    }//GEN-LAST:event_btnSalvarActionPerformed

